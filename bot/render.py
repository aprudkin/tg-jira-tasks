"""Общие помощники рендеринга Telegram-сообщений.

Чистая презентация без зависимостей от bot.* — единый владелец того, как выглядит
ссылка на задачу (её переиспользуют и список задач, и уведомления).
"""
from aiogram.utils.markdown import hlink


def issue_ref(key: str, url: str, summary: str) -> str:
    """Ссылка на задачу одной строкой: «<ключ>: <краткое описание>» (ключ — гиперссылка)."""
    return f"{hlink(key, url)}: {summary}"
